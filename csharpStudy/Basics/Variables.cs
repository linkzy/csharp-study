using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace csharpStudy.Basics
{
    internal class Variables
    {
        public Variables()
        {
            // Variables
            int age = 25;           // Integer variable
            double salary = 50000.50;// Double-precision floating-point variable
            string name = "John";   // String variable
            bool isStudent = false;  // Boolean variable

            // Data Types
            int integerValue = 5;                   // Integer
            double doubleValue = 3.14;              // Double-precision floating-point
            string stringValue = "Hello, C#!";      // String
            bool boolValue = true;                  // Boolean

            // Operators
            int x = 10;
            int y = 5;

            int sum = x + y;        // Addition
            int difference = x - y; // Subtraction
            bool isEqual = x == y;  // Equality check

        }
    }
}
